# You can look at the loop values within the loop
for steps in range(4):
    print(steps)

# if you want, you can start counting from 1
for steps in range(1,4):
    print(steps)

# if you want, you can tell the loop the step value
for steps in range(0,50,10):
    print(steps)

# you can tell the loop what values exactly 
# want to see in the loop
for steps in [1,2,3,4,5]:
    print(steps)